package com.utclo23.data.structure;

/**
 *
 * @author lucillefargeau
 */
public class Coordinate extends SerializableEntity{
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    
    
    
}
